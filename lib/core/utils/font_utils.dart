import 'package:flutter/material.dart';


TextStyle arabicStyle({
  double fontSize = 14,
  FontWeight fontWeight = FontWeight.w400,
  Color color = const Color(0xFF1A1A2E),
}) => TextStyle(
  fontSize: fontSize,
  fontWeight: fontWeight,
  color: color,
);

TextStyle latinStyle({
  double fontSize = 14,
  FontWeight fontWeight = FontWeight.w400,
  Color color = const Color(0xFF1A1A2E),
}) => TextStyle(
  fontSize: fontSize,
  fontWeight: fontWeight,
  color: color,
);
